// @ts-check
/* eslint-disable no-console */
const path = require("path");
const FastGlob = require("fast-glob");

/**
 * Normalize a docs file path into a Docusaurus docId.
 * @param {string} absFile
 * @param {string} docsDirectory
 * @returns {string}
 */
function toDocId(absFile, docsDirectory) {
    const rel = path.relative(docsDirectory, absFile);
    const noExt = rel.replace(/\.(md|mdx)$/i, "");
    return noExt.split(path.sep).join("/");
}

/**
 * Convert a docId's basename into a human label.
 * @param {string} docId
 * @returns {string}
 */
function toTitleCaseLabel(docId) {
    const base = docId.split("/").pop() || docId;
    return base
        .replace(/[-_]+/g, " ")
        .replace(/\s+/g, " ")
        .trim()
        .replace(/\b\w/g, (m) => m.toUpperCase());
}

/**
 * @typedef {{ docId: string; label: string; tags: string[] }} GlossaryTerm
 */

/**
 * @param {import('@docusaurus/types').LoadContext} loadContext
 * @param {{ docsDirectory: string }} options
 */
module.exports = function glossaryTermsPlugin(loadContext, options) {
    const pluginName = "ak-glossary-terms-plugin";

    return {
        name: pluginName,

        /**
         * @returns {Promise<{ terms: GlossaryTerm[] }>}
         */
        async loadContent() {
            console.log(`🚀 ${pluginName} loaded (siteDir=${loadContext.siteDir})`);
            const { docsDirectory } = options;
            try {
                /** @type {string[]} */
                const entries = /** @type {any} */ (
                    await FastGlob(["core/glossary/terms/**/*.{md,mdx}"], {
                        cwd: docsDirectory,
                        onlyFiles: true,
                        absolute: true,
                    })
                );

                /** @type {GlossaryTerm[]} */
                const terms = entries
                    .map((abs) => toDocId(abs, docsDirectory))
                    .map((docId) => ({ docId, label: toTitleCaseLabel(docId), tags: [] }))
                    .sort((a, b) =>
                        a.label.localeCompare(b.label, undefined, { sensitivity: "base" }),
                    );

                console.log(`✅ glossary terms collected: ${terms.length}`);
                return { terms };
            } catch (e) {
                // In case fast-glob or path handling fails, surface a readable error
                const err = e instanceof Error ? e : new Error(String(e));
                /** @type {string} */
                const message = `${pluginName}: Failed to collect glossary terms under ${docsDirectory}: ${err.message}`;
                throw new Error(message);
            }
        },

        /**
         * @param {{ content: { terms: GlossaryTerm[] }, actions: import('@docusaurus/types').PluginContentLoadedActions }} params
         */
        async contentLoaded(params) {
            const { content, actions } =
                /** @type {{ content: { terms: GlossaryTerm[] }, actions: import('@docusaurus/types').PluginContentLoadedActions }} */ (
                    params
                );

            // Create a generated module that exposes static import loaders for each term MDX.
            const lines = [];
            lines.push("/* Generated by ak-glossary-terms-plugin */");
            lines.push("export const loaders = {");
            for (const term of content.terms) {
                const id = term.docId;
                const importPath = id; // relative to docs site root
                // Use @site alias (points to siteDir for this docs site)
                lines.push(
                    `  ${JSON.stringify(id)}: () => import(/* webpackChunkName: \"glossary-${id.replace(/[^a-z0-9_\-]/gi, "_")}\" */ '@site/${importPath}.mdx'),`,
                );
            }
            lines.push("};");

            const source = lines.join("\n");

            const importsPath = await actions.createData("ak-glossary-imports.gen.mjs", source);

            actions.setGlobalData({ ...content, importsPath });
            console.log(
                `📦 ${pluginName} setGlobalData with ${content.terms.length} terms (imports: ${importsPath})`,
            );
        },
    };
};
